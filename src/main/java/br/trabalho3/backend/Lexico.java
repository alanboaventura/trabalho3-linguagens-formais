    public Token nextToken() throws LexicalError {
        if (!hasInput())
            return null;

        int start = position;

        int state = 0;
        int lastState = 0;
        int endState = -1;
        int end = -1;

        while (hasInput()) {
            lastState = state;
            state = nextState(nextChar(), state);

            if (state < 0)
                break;

            else {
                if (tokenForState(state) >= 0) {
                    endState = state;
                    end = position;
                }
            }
        }
        if (endState < 0 || (endState != state && tokenForState(lastState) == -2)) {
            String textTemp = input.substring(start);
            int iFim = (textTemp.indexOf(" ") != -1 ? textTemp.indexOf(" ") : input.length() - 1) + 1;
            String palavra = input.substring(start, iFim);

            palavra = trataPalavraInvalida(palavra, SCANNER_ERROR[lastState], iFim);

            throw new LexicalError(SCANNER_ERROR[lastState] + ": " + palavra);
        }

        position = end;

        int token = tokenForState(endState);

        if (token == 0)
            return nextToken();
        else {
            String lexeme = input.substring(start, end);
            token = lookupToken(token, lexeme);
            return new Token(token, lexeme, start);
        }
    }

